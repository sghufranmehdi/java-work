public class L17_LogicalOperator {
    public static void main(String[] args) {
         boolean a = true;
         boolean b = false;
        System.out.println("For logical and");
         if (a&&b)//and table
         {
             System.out.println("y");
         }
         else
         {
             System.out.println("N");
         }

        System.out.println("For logical Or");
        if (a||b)//Or table
        {
            System.out.println("y");
        }
        else
        {
            System.out.println("N");
        }

    }
}
